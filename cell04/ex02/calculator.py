#!/usr/bin/env python3

first_number = int(input("Give me the first number: "))
seconde_number = int(input("Give me the second number: "))

print("Thank you!")

print(f"{first_number} + {seconde_number} = {first_number + seconde_number}")
print(f"{first_number} - {seconde_number} = {first_number - seconde_number}")
print(f"{first_number} / {seconde_number} = {first_number / seconde_number}")
print(f"{first_number} * {seconde_number} = {first_number * seconde_number}")
